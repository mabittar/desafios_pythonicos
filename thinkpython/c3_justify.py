def right_justify(msg):
    tam = len(msg)
    out = ' '*(70 - tam) + msg
    return out

print(right_justify('conta textooooooooooooooooo'))
print(right_justify('conta textoooooooooooooooo'))    
print(right_justify('conta textoooooooooooooooo'))
print(right_justify('conta textooooooooooooooo'))
